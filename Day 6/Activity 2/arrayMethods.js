let arr = [1, 2, 3, 4];

console.log("Original array : ", arr);

arr.push(10);
console.log("Array after pushing 10 to it : ", arr);

arr.pop();
console.log("Array after popping element : ", arr);

arr.shift();
console.log("Array after shifting it :  ", arr);

arr.unshift(12);
console.log("Array after unshifting 12 to it :  ", arr);
